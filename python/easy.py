# add two numbers
def addTwoNumbers(a, b):
  return a + b

print(addTwoNumbers(4, 4))

#subtract two numbers
def subtractTwoNumbers(a, b):
  return a - b

print(subtractTwoNumbers(4, 4))

# multiply two numbers
def multiplyTwoNumbers(a, b):
  return a * b

print(multiplyTwoNumbers(4, 4))


# divide two numbers
def divideTwoNumbers(a, b):
  return a / b

print(divideTwoNumbers(4, 4))

